const fs = require("fs");
const path = require("path");
const checker = require("license-checker");

const resourcesDir = path.join(__dirname, "..", "resources");
const publicDir = path.join(__dirname, "..", "public");
const projectRoot = path.join(__dirname, "..");
const outputTargets = [
  {
    jsonOut: path.join(resourcesDir, "THIRD_PARTY_LICENSES.json"),
    mdOut: path.join(resourcesDir, "THIRD_PARTY_LICENSES.md")
  },
  {
    jsonOut: path.join(publicDir, "THIRD_PARTY_LICENSES.json"),
    mdOut: path.join(publicDir, "THIRD_PARTY_LICENSES.md")
  }
];

const manualUpstreamEntries = [
  {
    name: "nanobot-upstream",
    version: "upstream",
    licenses: "See upstream repository license",
    repository: "https://github.com/HKUDS/nanobot",
    publisher: "HKUDS and contributors",
    email: "",
    url: "https://github.com/HKUDS/nanobot",
    licenseFile: "",
    path: "resources/TopoClaw"
  },
  {
    name: "openclaw-skill-conventions",
    version: "upstream",
    licenses: "See upstream repository license",
    repository: "https://github.com/openclaw/openclaw",
    publisher: "OpenClaw contributors",
    email: "",
    url: "https://github.com/openclaw/openclaw",
    licenseFile: "",
    path: "resources/TopoClaw/topoclaw/skills"
  }
];

function normalize(item) {
  const key = item.name || item.key || "";
  const atIndex = key.lastIndexOf("@");
  const pkgName = atIndex > 0 ? key.slice(0, atIndex) : key;
  const version = atIndex > 0 ? key.slice(atIndex + 1) : "";

  return {
    name: pkgName,
    version,
    licenses: item.licenses || "UNKNOWN",
    repository: item.repository || "",
    publisher: item.publisher || "",
    email: item.email || "",
    url: item.url || "",
    licenseFile: item.licenseFile || "",
    path: item.path || ""
  };
}

function toRepoRelative(p) {
  if (!p) return "";
  const absolute = path.resolve(p);
  const relative = path.relative(projectRoot, absolute);
  if (!relative || relative.startsWith("..")) return "";
  return relative.replace(/\\/g, "/");
}

function readLicenseText(licenseFilePath) {
  if (!licenseFilePath) return "";
  try {
    return fs.readFileSync(licenseFilePath, "utf8").replace(/\r\n/g, "\n").trim();
  } catch {
    return "";
  }
}

function toMarkdown(rows) {
  const lines = [
    "# Third-Party Licenses",
    "",
    "This file is auto-generated by `npm run licenses:generate`.",
    ""
  ];

  for (const row of rows) {
    lines.push(`${row.name} ${row.version || "unknown"} - ${row.licenses}`);
    if (row.repository) lines.push(row.repository);
    else if (row.url) lines.push(row.url);
    lines.push("");
    if (row.licenseText) lines.push(row.licenseText);
    else lines.push("License text not bundled; see repository for details.");
    lines.push("");
  }

  return lines.join("\n");
}

checker.init(
  {
    start: path.join(__dirname, ".."),
    production: true,
    excludePrivatePackages: true
  },
  (error, packages) => {
    if (error) {
      console.error("[licenses:generate] failed:", error);
      process.exit(1);
    }

    const rows = Object.entries(packages)
      .map(([key, value]) => normalize({ ...value, key }))
      .concat(manualUpstreamEntries.map((item) => normalize(item)))
      .map((item) => {
        const licenseText = readLicenseText(item.licenseFile);
        return {
          ...item,
          licenseText,
          licenseFile: toRepoRelative(item.licenseFile),
          path: toRepoRelative(item.path)
        };
      })
      .sort((a, b) => `${a.name}@${a.version}`.localeCompare(`${b.name}@${b.version}`));

    for (const target of outputTargets) {
      fs.mkdirSync(path.dirname(target.jsonOut), { recursive: true });
      fs.writeFileSync(target.jsonOut, `${JSON.stringify(rows, null, 2)}\n`, "utf8");
      fs.writeFileSync(target.mdOut, `${toMarkdown(rows)}\n`, "utf8");
    }

    console.log(`[licenses:generate] wrote ${rows.length} entries`);
    for (const target of outputTargets) {
      console.log(`[licenses:generate] ${target.jsonOut}`);
      console.log(`[licenses:generate] ${target.mdOut}`);
    }
  }
);

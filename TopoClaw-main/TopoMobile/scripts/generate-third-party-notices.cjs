const fs = require("fs");
const path = require("path");
const checker = require("license-checker");

const projectRoot = path.join(__dirname, "..");
const appGradle = path.join(projectRoot, "app", "build.gradle");
const outputFile = path.join(projectRoot, "app", "src", "main", "assets", "THIRD_PARTY_NOTICES.txt");

const LICENSE_RULES = [
  { test: ({ group }) => group.startsWith("androidx."), license: "Apache-2.0", source: "AndroidX" },
  { test: ({ group, artifact }) => group === "com.google.android.material" || artifact === "material", license: "Apache-2.0", source: "Material Components for Android" },
  { test: ({ group }) => group === "com.squareup.retrofit2", license: "Apache-2.0", source: "Retrofit" },
  { test: ({ group }) => group === "com.squareup.okhttp3", license: "Apache-2.0", source: "OkHttp" },
  { test: ({ group, artifact }) => group === "org.jetbrains.kotlinx" && artifact.includes("coroutines"), license: "Apache-2.0", source: "Kotlin Coroutines" },
  { test: ({ group }) => group === "io.noties.markwon", license: "Apache-2.0", source: "Markwon" },
  { test: ({ group }) => group === "com.github.bumptech.glide", license: "BSD, MIT and Apache-2.0 (upstream mixed)", source: "Glide" },
  { test: ({ group }) => group.startsWith("com.google.zxing") || group.startsWith("com.journeyapps"), license: "Apache-2.0", source: "ZXing" },
  { test: ({ group, artifact }) => group === "org.tensorflow" && artifact.startsWith("tensorflow-lite"), license: "Apache-2.0", source: "TensorFlow Lite" }
];

function parseGradleDependencies(content) {
  const deps = [];
  const regex = /^\s*implementation\s+['"]([^:'"]+):([^:'"]+):([^'"]+)['"]/gm;
  for (const match of content.matchAll(regex)) {
    const group = match[1];
    const artifact = match[2];
    const version = match[3];
    deps.push({ group, artifact, version });
  }
  return deps;
}

function inferLicense(dep) {
  for (const rule of LICENSE_RULES) {
    if (rule.test(dep)) {
      return { license: rule.license, source: rule.source };
    }
  }
  return { license: "UNKNOWN (please verify upstream)", source: dep.group };
}

function formatNotices(androidDeps) {
  const lines = [
    "TopoClaw Android - Third-Party Components and Open Source Notices",
    "This file is auto-generated by `npm run licenses:generate`.",
    "",
    "A. Android dependencies",
    "==============================",
    ""
  ];

  androidDeps.forEach((dep, index) => {
    const inferred = inferLicense(dep);
    lines.push(
      `${index + 1}) ${dep.group}:${dep.artifact}:${dep.version}`,
      `   License: ${inferred.license}`,
      `   Source: ${inferred.source}`,
      ""
    );
  });

  lines.push(
    "B. Data and bundled SDK notes",
    "==============================",
    ""
  );

  const bundledNotes = [
    {
      title: "pinyin-data (https://github.com/mozillazg/pinyin-data)",
      license: "Refer to upstream repository"
    },
    {
      title: "Rime-ice (https://github.com/iDvel/rime-ice)",
      license: "Refer to upstream repository"
    }
  ];

  if (fs.existsSync(path.join(projectRoot, "app", "libs", "SparkChain.aar"))) {
    bundledNotes.push({
      title: "SparkChain SDK (app/libs/SparkChain.aar)",
      license: "Vendor agreement / proprietary terms"
    });
  }

  bundledNotes.forEach((note, idx) => {
    lines.push(
      `${androidDeps.length + idx + 1}) ${note.title}`,
      `   License: ${note.license}`,
      ""
    );
  });

  return lines.join("\n") + "\n";
}

function run() {
  const gradleText = fs.readFileSync(appGradle, "utf8");
  const androidDeps = parseGradleDependencies(gradleText).sort((a, b) =>
    `${a.group}:${a.artifact}`.localeCompare(`${b.group}:${b.artifact}`)
  );

  checker.init(
    {
      start: projectRoot,
      production: false,
      excludePrivatePackages: true
    },
    (error, packages) => {
      if (error) {
        console.error("[licenses:generate] license-checker failed:", error);
        process.exit(1);
      }

      const npmRows = Object.entries(packages || {})
        .map(([key, value]) => {
          const at = key.lastIndexOf("@");
          const name = at > 0 ? key.slice(0, at) : key;
          const version = at > 0 ? key.slice(at + 1) : "";
          return {
            name,
            version,
            licenses: value.licenses || "UNKNOWN",
            repository: value.repository || ""
          };
        })
        .filter((row) => row.name !== "topoclaw-mobile-license-tools")
        .sort((a, b) => `${a.name}@${a.version}`.localeCompare(`${b.name}@${b.version}`));

      fs.mkdirSync(path.dirname(outputFile), { recursive: true });
      fs.writeFileSync(outputFile, formatNotices(androidDeps), "utf8");
      console.log(`[licenses:generate] wrote ${androidDeps.length} Android deps and ${npmRows.length} npm deps`);
      console.log(`[licenses:generate] ${outputFile}`);
    }
  );
}

run();
